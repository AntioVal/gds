    public void Limpia() {
        this.jComboBox.setModel(new DefaultComboBoxModel(c.getCuentasOrigen()));
        mitxtCuentaOrigen.setText("");
        mitxtNumCuenta.setText("");
        mitxtClaveCuenta.setText("");
        this.jComboBox.grabFocus();
    }
    private void mitxtCuentaOrigenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mitxtCuentaOrigenActionPerformed
        String cuenta = mitxtCuentaOrigen.getText().toUpperCase().toString().trim();
        boolean valida_cuenta = c.validaCuentaOrigen(cuenta);
        if (!cuenta.equals("")) {
            if (!valida_cuenta) {
                this.mitxtNumCuenta.grabFocus();
            } else {
                JOptionPane.showMessageDialog(this, " Cuenta de origen existente, favor de verificar ", "Alerta", JOptionPane.WARNING_MESSAGE);
                this.mitxtCuentaOrigen.setText("");
                this.mitxtCuentaOrigen.grabFocus();
            }
        } else {
            this.mitxtNumCuenta.grabFocus();
        }
}//GEN-LAST:event_mitxtCuentaOrigenActionPerformed

    private void mitxtNumCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_mitxtNumCuentaActionPerformed
        String num_cuenta = mitxtNumCuenta.getText().toUpperCase().toString().trim();
        Double tmp = 0.0D;
        if (!num_cuenta.equals("")) {
            try {
                if (num_cuenta.length() == 13) {
                    tmp = Double.parseDouble(num_cuenta);
                    if (tmp > 0.0) {
                        if (num_cuenta.length() == 13) {
                            mitxtClaveCuenta.grabFocus();
                        } else {
                            JOptionPane.showMessageDialog(this, " El número de cuenta debe ser de 13 dígitos  ", "Alerta", JOptionPane.WARNING_MESSAGE);
                            mitxtNumCuenta.setText("");
                            mitxtNumCuenta.grabFocus();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, " El número de cuenta debe ser mayor a 0  ", "Alerta", JOptionPane.WARNING_MESSAGE);
                        mitxtNumCuenta.setText("");
                        mitxtNumCuenta.grabFocus();
                    }
                } else {
                    JOptionPane.showMessageDialog(this, " El número de cuenta debe ser de 13 dígitos  ", "Alerta", JOptionPane.WARNING_MESSAGE);
                    mitxtNumCuenta.setText("");
                    mitxtNumCuenta.grabFocus();
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, " Formato inválido ", "Error", JOptionPane.ERROR_MESSAGE);
                mitxtNumCuenta.setText("");
                mitxtNumCuenta.grabFocus();
            }
        } else {
            mitxtClaveCuenta.grabFocus();
        }

}//GEN-LAST:event_mitxtNumCuentaActionPerformed


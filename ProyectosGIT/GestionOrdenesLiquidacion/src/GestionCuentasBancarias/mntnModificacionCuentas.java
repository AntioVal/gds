    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed

        cuenta_origen = this.jComboBox.getSelectedItem().toString();
        String cuenta = this.mitxtCuentaOrigen.getText().toUpperCase().toString().trim();
        String num_cuenta = this.mitxtNumCuenta.getText().toString().trim();
        String clave_cuenta = this.mitxtClaveCuenta.getText().toString().trim();
        boolean guarda = false;
        Double val = 0.0D;
        try {
            if (cuenta_origen.equals("   -----Selecciona Cuenta-----   ")) {
                JOptionPane.showMessageDialog(this, " Favor de Seleccionar Cuenta ", "Alerta", JOptionPane.WARNING_MESSAGE);
                this.jComboBox.grabFocus();
            } else {
                if (!num_cuenta.equals("")) {
                    if (num_cuenta.length() == 13) {
                        val = Double.parseDouble(num_cuenta);
                        guarda = clsModelo.actualizaCuenta(cuenta_origen, cuenta, num_cuenta, clave_cuenta);
                        if (guarda) {
                            JOptionPane.showMessageDialog(this, " Operación realizada satisfactoriamente ", "Información", JOptionPane.INFORMATION_MESSAGE);
                            Limpia();
                        } else {
                            JOptionPane.showMessageDialog(this, " Error actualizando información ", "Error", JOptionPane.ERROR_MESSAGE);
                            Limpia();
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, " El número de cuenta debe ser de 13 dígitos ", "Alerta", JOptionPane.WARNING_MESSAGE);
                        this.mitxtNumCuenta.setText("");
                        this.mitxtNumCuenta.grabFocus();
                    }
                } else {
                    guarda = clsModelo.actualizaCuenta(cuenta_origen, cuenta, num_cuenta, clave_cuenta);
                    if (guarda) {
                        JOptionPane.showMessageDialog(this, " Operación realizada satisfactoriamente ", "Información", JOptionPane.INFORMATION_MESSAGE);
                        Limpia();
                    } else {
                        JOptionPane.showMessageDialog(this, " Error actualizando información ", "Error", JOptionPane.ERROR_MESSAGE);
                        Limpia();
                    }
                }

            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, " Formato inválido en número de cuenta ", "Error", JOptionPane.ERROR_MESSAGE);
            this.mitxtNumCuenta.setText("");
            this.mitxtNumCuenta.grabFocus();
        }
}//GEN-LAST:event_jButtonActionPerformed


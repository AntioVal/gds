    private void jButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActionPerformed
        String cuenta_origen = jComboBox.getSelectedItem().toString();
        if (!cuenta_origen.equals("   -----Selecciona Cuenta-----   ")) {
            boolean baja = c.cancelaCuenta(cuenta_origen);
            if (baja) {
                JOptionPane.showMessageDialog(this, " Operaci칩n realizada satisfactoriamente ", "Informaci칩n", JOptionPane.INFORMATION_MESSAGE);
                this.jComboBox.setModel(new DefaultComboBoxModel(c.getCuentasOrigen()));
                this.jComboBox.grabFocus();
            } else {
                JOptionPane.showMessageDialog(this, " Error actualizando informaci칩n ", "Error", JOptionPane.ERROR_MESSAGE);
                this.jComboBox.grabFocus();
            }
        } else {
            JOptionPane.showMessageDialog(this, " Favor de especificar una cuenta ", "Alerta", JOptionPane.WARNING_MESSAGE);
            this.jComboBox.grabFocus();
        }

    }//GEN-LAST:event_jButtonActionPerformed

